// age
var myAge = 21;
// early years
let earlyYears = 2;
earlyYears *= 10.5;
// later years
var laterYears = myAge - 2;
// later years * 4
laterYears *= 4;

// my age in dog years
var myAgeInDogYears = earlyYears + laterYears;

// my name lower case
var myName = 'Matteo'.toLowerCase();

// c.log of final string
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);